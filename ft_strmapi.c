#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	char			*ptr;
	unsigned int	i;

	i = 0;
	if (!s)
		return (0);
	if (!(ptr = (char *)malloc(sizeof(*ptr) * (ft_strlen(s) + 1))))
		return (NULL);
	while (s[i])
	{
		ptr[i] = f(i, s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
